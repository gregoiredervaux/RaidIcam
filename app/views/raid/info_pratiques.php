		</h2>

		<p>
			La manifestation se déroule par équipe de 4, mixte ou non, sans remplaçant. Il est obligatoire de participer aux épreuves avec l’ensemble des membres de l’équipe. Si cette règle n’est pas respectée, des pénalités seront appliquées.
			L’ensemble du Raid est chronométré à l’aide d’un doigt électronique remis à chaque équipe lors du check administratif. (un doigt par personne pour le parcours expert, un par équipe pour le parcours aventure)
			Tous les participants doivent être au moins dans l'année de leur 18 ans au 17 mars 2018 (soit née avant le 31/12/2000).
			Si le particiapant est dans l'année de ses 18 ans, mais n'est pas majeur, celui-ci doit remplir et nous faire parvenir <a href="/library/Autorisation_parentale_Raid_ICAM_2018.pdf">une autorisation parentale</a>
